# Python3 Loops
# -------------

# For in loop
fruits = ['banana', 'apple',  'mango']
for fruit in fruits:
   print ('Current fruit :', fruit)
print ('Done with loop.') # This will be part of the loop if not 1 tab behind the "looping print"


# Index loops
fruits = ['banana', 'apple',  'mango']
for index in range(len(fruits)):
   print ('Current fruit :', fruits[index])  
print ('Done with loop.')